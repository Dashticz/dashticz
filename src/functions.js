function setHeight(obj) {
  return isDefined(obj.block.height) && obj.block.height
    ? obj.block.height
    : Math.min(
        Math.round(
          ($(obj.mountPoint).width() / window.innerWidth) * window.innerHeight -
            25
        ),
        window.innerHeight - 50
      );
}